function suma(a, b){
    let resultadoSuma = a + b
    return resultadoSuma
}

function resta(a, b){
    let resultadoResta = a - b
    return resultadoResta
}

function multi(a, b){
    let resultadoMulti = a * b
    return resultadoMulti
}

function divis(a, b){
    let resultadoDiv = a / b
    return resultadoDiv
}

module.exports = {suma, resta, multi, divis}